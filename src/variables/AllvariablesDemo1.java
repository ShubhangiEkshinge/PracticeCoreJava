//Example of using all variables
package variables;

public class AllvariablesDemo1 {
	int k = 100; // Instance variable
	static short s = 101; // Static variable

	public static void main(String[] args) {
			long l = 20l; // Local variable
		AllvariablesDemo1 f = new AllvariablesDemo1();
		System.out.println(f.k);
		System.out.println(s);
		System.out.println(l);

	}
}
