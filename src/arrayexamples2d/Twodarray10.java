package arrayexamples2d;

public class Twodarray10 {

	public static void main(String[] args) {
		int[][] x; // defined x
		x = new int[4][3]; // declared x

		x[0][0] = 141;
		x[2][1] = 125;
		x[3][2] = 156;
		System.out.println(x[0][0]);
		System.out.println(x[2][1]);
		System.out.println(x[3][2]);
	}

}
